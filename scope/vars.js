// Asignación y declaración 

var firstName; //Undefined 

firstName = 'Oscar';

console.log(firstName); // Oscar

var lastName = 'David'; // Declarar/Asignar

lastName = 'Ana'; // Reasignar

console.log(lastName); // Nombrar

// Cumplir con el standard

//Let

let fruit = 'apple'; // Declara y asigna
fruit = 'kiwi'; // Reasigna
// let fruit = 'Banana';  No se puede redeclarar

// const

const animal = 'dog';
// const animal = 'cat'; No se puede ni reasignar ni redeclarar

// Hay que tener en cuenta que cuando se realiza un array dentro de un const o let se crean valores de referencia que pueden ser modificados. 
